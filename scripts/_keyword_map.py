def _label_priority(label: str) -> int:
    for lbl, _, p in KEYWORD_RULES:
        if lbl == label:
            return p
    return 0


def match_keyword_reason(text: str) -> tuple[str, str, int] | None:
    """텍스트에서 가장 우선순위 높은 카테고리 1개 반환.

    Args:
        text: 검색 대상 (뉴스 제목·본문 등 합친 한 문자열).

    Returns:
        (label, matched_keyword, priority) or None
    """
    if not text:
        return None
    text_l = text.lower()
    best: tuple[str, str, int] | None = None
    for label, kws, prio in KEYWORD_RULES:
        for kw in kws:
            if kw.lower() in text_l:
                if best is None or prio > best[2]:
                    best = (label, kw, prio)
                break
    return best


def confidence_from_priority(priority: int) -> str:
    """우선순위 → 신뢰도 라벨."""
    if priority >= 9:
        return 'high'
    if priority >= 7:
        return 'mid'
    return 'low'
